package pl.TypyDanychZmienne;

public class DataTypes {
    public static void main(String[] args) {
        System.out.println(1_000_000_000);
        System.out.println(1.0/3.0);
        System.out.println(1.0F/3.0F);
        System.out.println('\u2602');
        System.out.println(true);
        System.out.println(false);
        System.out.print("Witaj \tświecie!\n");
        System.out.println("Witaj \"świecie\"");
        System.out.print(123);

        System.out.println("Cześć Jan, słyszałem, że masz " + 25 + " lat i uczysz się programowania. Czy to prawda?");
        System.out.println(true);
        System.out.print("Czy twoje inicjały to ");
        System.out.print('J');
        System.out.print(" ");
        System.out.print('K');
        System.out.println('?');
        System.out.println("Tak, " + true);

    }

}
